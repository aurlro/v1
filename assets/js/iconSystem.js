/**
 * Récupère une icône SVG par clé
 * @param {string} iconKey - Clé de l'icône
 * @param {object} options - Options {size, class, color}
 * @returns {string} SVG HTML
 */
function getIcon(iconKey, options = {}) {
    const iconFunction = ICON_SET[iconKey];

    if (!iconFunction) {
        console.warn(`Icône non trouvée: ${iconKey}`);
        return '';
    }

    let svg = iconFunction();

    // Appliquer les options
    if (options.class) {
        svg = svg.replace('class="', `class="${options.class} `);
    }

    if (options.size) {
        svg = svg.replace(/w-\d+\s+h-\d+/g, `w-${options.size} h-${options.size}`);
    }

    return svg;
}

/**
 * Crée un élément d'icône avec options
 */
function createIconElement(iconKey, options = {}) {
    const div = document.createElement('span');
    div.innerHTML = getIcon(iconKey, options);
    div.className = options.containerClass || '';
    return div.firstChild;
}

